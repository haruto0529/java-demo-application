package turnGame;

public class Charactor {
	protected String name;
	protected int hp;
	protected int atk;
	protected boolean isCharging;


	public Charactor(String name, int hp, int atk, boolean isCharging) {
		super();
		this.name = name;
		this.hp = hp;
		this.atk = atk;
		this.isCharging = isCharging;
	}
	
	

}
